$('.info').popover();

$(':not(#anything)').on('click', function (e) {
    $('.info').each(function () {
        if (!$(this).is(e.target) && $(this).has(e.target).length === 0 && $('.popover').has(e.target).length === 0) {
            $(this).popover('hide');
            return;
        }
    });
});